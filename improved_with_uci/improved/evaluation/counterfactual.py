"""
counterfactual.py
=================
Intervention sensitivity analysis.

FIX over original:
  - Original added a constant offset to ALL timesteps simultaneously,
    bypassing the progression model and producing unrealistically uniform shifts.
  - New version uses the TWO parallel trajectory branches generated by
    clinical_temporal.py (X_base, X_interv) — which correctly encode
    the exponential ramp-up lag — and passes both through the model.
  - Reports BR and BR_velocity metrics alongside the raw risk delta.
"""

import numpy as np


def simulate_intervention(model, X_base, X_interv, users):
    """
    Parameters
    ----------
    model      : trained temporal model (PyTorch, eval mode)
    X_base     : (N, T, F) tensor  — baseline trajectories
    X_interv   : (N, T, F) tensor  — intervention trajectories (lagged ramp-up)
    users      : (N,) user IDs

    Returns
    -------
    dict with:
      risk_base   : (N, 3)   baseline predicted risk
      risk_interv : (N, 3)   post-intervention predicted risk
      delta       : (N, 3)   risk_interv - risk_base
      BR          : float    behavioural robustness score (mean |delta|)
    """
    import torch
    from utils.metrics import behavioural_robustness, intervention_significance

    model.eval()
    with torch.no_grad():
        R_base   = model(X_base,   users).numpy()
        R_interv = model(X_interv, users).numpy()

    delta = R_interv - R_base

    # Behavioural robustness uses the first output (7-day risk) for trajectory
    br    = behavioural_robustness(R_base[:, 0], R_interv[:, 0])
    stats = intervention_significance(R_base[:, 0], R_interv[:, 0])

    return {
        "risk_base":   R_base,
        "risk_interv": R_interv,
        "delta":       delta,
        "BR":          br["BR"],
        "BR_velocity": br["BR_velocity"],
        "mean_delta":  float(delta.mean()),
        "p_value":     stats["p_value"],
        "relative_%":  stats["relative_change_%"],
    }


def print_intervention_report(result: dict):
    print("\n─── Intervention Sensitivity Report ──────────────────────")
    print(f"  Mean risk (baseline)     : {result['risk_base'].mean():.4f}")
    print(f"  Mean risk (intervention) : {result['risk_interv'].mean():.4f}")
    print(f"  Mean Δ R                 : {result['mean_delta']:+.6f}")
    print(f"  Relative change          : {result['relative_%']:+.3f} %")
    print(f"  Behavioural Robustness   : {result['BR']:.6f}")
    print(f"  BR_velocity              : {result['BR_velocity']:.6f}")
    print(f"  Wilcoxon p-value         : {result['p_value']:.4e}")
    print("──────────────────────────────────────────────────────────\n")
